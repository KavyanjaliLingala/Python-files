#Comparison Operators
num1=40
num2=70
print(num1)
print(num2)
#Comparison Equalto Operator
print(num1==num2)
#Comparison NotEqualto Operator
print(num1!=num2)
#Comparison Lessthan Operator
print(num1<num2)
#Comparison Greaterthan Operator
print(num1>num2)
#Comparison LessthanorEqualto Operator
print(num1<=num2)
#Comparison GreaterthanorEqualto Operator
print(num1>=num2)
