#Bitwise Operators
#AND &
a=12#(multiplication of two binary values)
b=5
c=a&b
print(c)

#OR |
a=5 #(Addition of two binary values)
b=4
a|=b
print(a)

#XOR ^
a^=b
print(a)

#Leftshift <<( here a is convereted into binary value and b is the no. of values added to its left )
c=a<<b
print(c)

#Rightshift>>
a>>=b
print(a)


